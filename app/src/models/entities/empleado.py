class Empleado:
    def __init__(self, DNI, nombres, primerApellido, segundoApellido, fecNacimiento, sexo, telefono, correo, direccion, salario, puesto, aniosExperiencia, codHorario, codEmpleado=0):
        self.DNI = DNI
        self.nombres = nombres
        self.primerApellido = primerApellido
        self.segundoApellido = segundoApellido
        self.fecNacimiento = fecNacimiento
        self.sexo = sexo
        self.telefono = telefono
        self.correo = correo
        self.direccion = direccion
        self.salario = salario
        self.puesto = puesto
        self.aniosExperiencia = aniosExperiencia
        self.codHorario = codHorario
        self.codEmpleado = codEmpleado
